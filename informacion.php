        <p>
          Aqui explicare informacion importante a tener en cuenta
          sobre las computadoras.
        </p><br>
        <h4> COMPONENTES DE LA COMPUTADORA </h4><br>
        <p>
          <ul>
            <li> <u><b>Procesador</b></u>  </li>
            <p>Es el encargado de procesar todos los datos,
              se puede decir que es el cerebro de la computadoras
              y el componente mas importante.
            </p>

            <li> <u><b>Placa madre</b></u> </li>
            <p>Es una pieza fundamental por que en esta placa se integran
              todos los demas componentes de la computadora, esta misma
              contiene circuitos electronicos y varios conectores para los perofericos de E/S. 
            </p>

            <li><u><b>Memoria RAM</b></u></li>
            <p>La memoria RAM es un tipo de memoria volatil, osea que todos sus datos se borran
              cuando se apaga la computadora pero mientras funciona se almacenan de forma 
              temporal los datos de los programas que estás utilizando en este momento.
            </p>

            <li><u><b>Disco duro</b></u></li>
            <p>
            Un disco duro es un dispositivo para el almacenamiento de datos de forma 
            no volátil, es decir, para almacenar los datos digitales utiliza un sistema de grabación magnética.
            </p>

            <li><u><b>Fuente de alimentacion</b></u></li>
            <p>
            Se encarga de transformar una corriente eléctrica alterna en una corriente eléctrica continua transmitiendo 
            la corriente eléctrica imprescindible y necesaria a los ordenadores para el buen funcionamiento y protección de estos.
            </p>

            <li><u><b>Gabinete</b></u></li>
            <p>
            Es el armazón que contiene los principales componentes de hardware de una computadora: su CPU, tarjeta madre, microprocesador, 
            memoria, disco rígido y unidades internas (lector de CD o DVD, etc.). La principal función del gabinete es proteger a estos componentes.
            </p>
          </ul>
        </p>

